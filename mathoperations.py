#Addition
print(2 + 3)  # Output: 5

#Subtraction    
print(5 - 2)  # Output: 3

#Multiplication
print(4 * 3)  # Output: 12

#Division
print(10 / 2)  # Output: 5.0

#Floor Division
print(10 // 3)  # Output: 3

#Modulus
print(10 % 3)  # Output: 1

#Exponentiation
print(2 ** 3)  # Output: 8

#Operator Precedence
print(2 + 3 * 4)      # Output: 14      

print((2 + 3) * 4)    # Output: 20