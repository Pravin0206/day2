# Type() function

print(type(42))          # <class 'int'>
print(type(3.14))       # <class 'float'>
print(type("Hello"))    # <class 'str'>
print(type(True))       # <class 'bool'>


#Type Conversion

print(int(3.99))        # 3

print("Hello your name is of " + str(len(input("Enter your name: "))) + " characters")  # Converts length to string for concatenation